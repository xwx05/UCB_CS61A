# Q3: Is Prime?
def is_prime(n):
    """
    >>> is_prime(10)
    False
    >>> is_prime(7)
    True
    >>> is_prime(1) # one is not a prime number!!
    False
    """
    flag = False
    if n == 1:
        return flag
    for i in range(2, n):
        if n % i == 0:
            return flag
    flag = True
    return flag


# Q4: Unique Digits
def unique_digits(n):
    """Return the number of unique digits in positive integer n.

    >>> unique_digits(8675309) # All are unique
    7
    >>> unique_digits(13173131) # 1, 3, and 7
    3
    >>> unique_digits(101) # 0 and 1
    2
    """
    count = 0
    digit_list = []
    while n > 0:
        digit = n % 10
        if digit not in digit_list:
            count += 1
            digit_list.append(digit)
        n = n // 10
    return count


def has_digit(n, k):
    """Returns whether k is a digit in n.

    >>> has_digit(10, 1)
    True
    >>> has_digit(12, 7)
    False
    """
    assert k >= 0 and k < 10
    flag = False
    while n > 0:
        if n % 10 == k:
            flag = True
            break
        else:
            n = n // 10
    return flag
